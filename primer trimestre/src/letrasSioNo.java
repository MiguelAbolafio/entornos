
public class letrasSioNo {

	public static void main(String[] args) {
	
		boolean centinela;
		centinela=true;
		char letra = 'b';
		
		
	
		
		System.out.println("¿Lo siguiente es la letra A o B?: "+letra);
		
		
		
		switch(letra) {
		
		case 'a':
		System.out.println(centinela);
		break;
		case 'b':
		System.out.println(centinela);
		break;
		default:
			centinela=false;
			System.out.println(centinela);
		}
		
	}

}
